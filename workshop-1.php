        <h1>Synthetic Aperture Radar (SAR) Technology</h1>
        <p class="speaker-info"><strong>Speaker:</strong> Dr. Tapan Misra, DS & Former Director, SAC, Founding Director & Chief Scientist, SISIR Radar</p>
        <p class="time-venue"><strong>Time:</strong> 8.30-10.30 Hrs &nbsp;&nbsp;&nbsp;&nbsp;<strong>Venue:</strong> Amaryllis & Petunia</p>
        <h3>Abstract:</h3>
        <p>In the last seven decades, Synthetic Aperture Radar (SAR) developed into a formidable tool for imaging earth, planetary objects and recently even imaging Event Horizon of black holes. Unlike optical imaging systems, SAR picks up structural signatures from interaction of different polarisations of electromagnetic waves to structural features. SAR signature is invariant to day and night, diurnal and seasonal variations. Further, the signature is almost unaffected by clouds, dusts, water vapours and other vagaries of weather, making SAR a prized imaging sensor. Consistency of SAR images, unlike optical imaging, makes it very much amenable to AI/ML. But SAR sensor itself is a bunch of contradictions. SAR is nothing but a pulse Doppler radar in motion. Because of motion, SAR signatures can be used to create a virtual array antenna of a very very large length and hence very fine beamwidth on a computer post facto. Hence the nomenclature of SAR. But SAR utilises clutter signal to create an image. But clutter is anathema to Radar engineers and an array of Radar literature is devoted to different techniques to eliminate clutter!! Similarly antenna engineers are accustomed to only array antennas operating in the far field of the Array antenna itself. However, the SAR antenna actually operates mostly in the reactive field region and rarely even in Near Field region of the Synthetic Array antenna itself. SAR array poses a significant contradiction to conventional knowledge of antenna operation. SAR antenna is formed post facto on computer. Surprisingly, the tool used for array synthesis of SAR is signal processing. The formidable signal processing requirements call for huge computing resources and clever software strategies to handle extremely large computation and back and forth exchange of large data multiple times. Further, over last five decades, computing hardware and computing architectures have evolved beyond imaginations. Consequently, SAR processing algorithms and software had to adapt repeatedly to match evolution of computing and storage systems. We all know that final geometric accuracies of optical images depend upon errors in three degrees of angular rotation and three degrees of positional estimates. Surprisingly, location accuracy of SAR images is governed by only errors in positon estimates but not at all on errors of angular estimates. In fact most SAR processors do not even use any data from angle sensors of SAR platform!! Superiority of SAR images accuracy makes it a "must have" sensor in every advanced fighter aircraft and reconnaissance platform. No doubt, SAR is a fascinating sensor and it is still evolving. This particular workshop talk will address various facets of SAR technology, their evolution and also signatures and applications of SAR imaging.</p>
        <h3>Bio:</h3>
        <p>Mr. Tapan Misra started his career in Space Applications Centre, ISRO in 1984 and since then played pivotal role in growth of Microwave Remote Sensing payloads and allied technologies in SAC and in India. He is widely known for design and development of C-band Synthetic Aperture Radar (SAR) of the RISAT-1 in 2012. He steered the development of RISAT-2 series of high resolution X-band SAR system. He conceptualised and led development of highly miniaturised Dual Frequency (L and S band) SAR (DFSAR) for Chandrayaan 2 orbiter (2019). He was also the principal designer of Ka band radio altimeter which enabled smooth landing of RLV and Chandrayaan 3 lander. He is also the principal architect of the novel sweepSAR of NISAR (NASA-ISRO SAR), likely to be launched in 2024. He was key contributor for system engineering of Multi-frequency Scanning Microwave Radiometer (MSMR) payload for Oceansat-1 during 1995-1999 and Scanning Scatterometer of Oceansat-2 in 2009. He was appointed as Director of Space Applications Centre (SAC) in February 2015. For a brief period he had additional charge of Director, Physical Research Laboratory, Ahmedabad from June 2016 to February 2017. He also headed the Office of Innovations Management, ISRO, Bangalore. Tapan Misra was appointed as a Senior Adviser to Chairman of ISRO in July 2018. He superannuated from ISRO in January 2021. Presently he is holding the position of Founding Director and Consultant Chief Scientist of a startup named SISIR Radar Pvt Ltd, based out of Kolkata. Under his leadership, SISIR Radar has emerged as one of the very few key Deep Tech startups in India, specializing in developing miniature SAR System, ultra wide band GPR systems and innovative Spaceborne SAR. He received Vikram Sarabhai Research Award in 2004 and ISRO Merit award in 2008 for his contribution of development of SAR technology. He was elected as Fellow of Indian National Academy of Engineering in 2007. He was elected Corresponding Member of International Academy of Astronautics in 2008. He is also elected Fellow of IETE and ISRS. He was awarded DSc (Hon.) in 2016 and Distinguished Alumnus award in 2017 by his Alma Mater, Jadavpur University. He chaired CGMS 2018 (Coordination Group for Meteorological Satellites) in Bangalore, represented India as Principal to CEOS (Committee on Earth Observation Satellites) in Paris in 2017 and participated as team member of Indo-US Strategic Dialogue on Space Cooperation in Washington DC in 2016 and 2018. He also taught at IIT Kharagpur and IIT Jodhpur as Adjunct Professor. He holds eight granted patents. He has five copyrights and more than fifty five papers to his credit.</p>
