    <div class="workshop-details">
        <h1>Create Physics-based High-Fidelity Space Mission Using Simulation</h1>
        <p class="speaker-info"><strong>Speakers:</strong> Mr. Nijas Kunju & Mr. Ravindra Challa, Ansys</p>
        <p class="time-venue"><strong>Time:</strong> 11.00-13.00 Hrs &nbsp;&nbsp;&nbsp;&nbsp;<strong>Venue:</strong> Amaryllis & Petunia</p>
        <h3>Abstract:</h3>
        <p>Space is always filled with mystery, and everyone is excited to hear about space exploration. Since there are many unknowns in space, the current achievements in space technologies are gathered from knowledge collected over the years by different countries. Unlike other areas, space organizations cannot afford failures in their missions, and first-time success is a major milestone. This workshop session will help the audience learn about the different stages of space missions and how simulation tools can help the space community model the entire mission virtually before creating a prototype. The agenda for the sessions is as follows:</p>
        <ul>
            <li>Create a space mission from the requirements (spacecraft launch, orbit determination, satellite orbital maneuvering).</li>
            <li>Protect satellites from harsh space environments.</li>
            <li>Check the performance of space communication, radar, and EOIR systems.</li>
        </ul>
        <h3>Bios:</h3>
        <p><strong>Nijas Kunju:</strong> Nijas Kunju is a Principal Application Engineer in Application Engineering at ANSYS Inc. He specialized in the domain of RF, Antennas, Radar, Signal Integrity, Power Integrity, and Multiphysics simulation. Nijas has been working with ANSYS for the last 8 years. He provides technical assistance for RF components, Antennas, Phased arrays, Radar, Automotive and 5G/6G applications. He has been interacting with different Aero and Space customers on various communication, Radar, and EWS system development through virtualization and digital engineering. Before joining ANSYS, he worked as a Research Consultant at GE Global Research in Bangalore in the area of Wireless Power Transfer and microwave material sensing for detecting different materials in Crude Oils. Nijas did his Master’s degree in Electronics Science and PhD under Prof. P. Mohanan from Cochin University of Science and Technology, Cochin, Kerala. He holds 20+ International publications and 20+ national and international conference publications.</p>
        <p><strong>Ravindra Challa:</strong> Ravindra Challa is a Senior Application Engineer at Ansys, where he leverages more than 11 years of experience bringing AGI’s digital mission engineering and aerospace systems simulation solutions to the global aerospace and defense community. With a career built on applying technical solutions as a Scientist in the Indian Space Research Organisation, Ravindra continues to apply a growing skill set to bring industry-leading simulation solutions to engineering organizations focused on digital transformation.</p>
    </div>
    <div class="workshop">
        <h2 class="title">Reliability and Quality Assurance for Spacecraft Systems</h2>
        <p class="speakers">Speakers: Shri. PJVKS Prakash Rao, Former Director, SIPO, ISRO HQ</p>
        <p class="time">Time: 14.00-16.00 Hrs</p>
        <p class="venue">Venue: Amaryllis & Petunia</p>
        <div class="abstract">
            <p><strong>Abstract:</strong> In today’s world, there is no discipline, where the word ‘Quality’ does not find a mention. Research, Design, Development, Production, Marketing, Service, Customer interaction etc are now more driven by quality concepts - than in yesteryears. While this focus on quality is relatively new to the commercial establishments, which are primarily driven by competition and globalisation, there are some disciplines of engineering/research like Defence and Aerospace Sectors, which due to their critical nature, had ingredients of quality and reliability built into their programmes, almost from inception. This talk gives a brief insight into various elements of R&QA, which need to be implemented for a spacecraft project. Technical drivers for quality programme of a spacecraft due to its unattended operation for long life duration under harsh environment of space are enumerated in the talk. Design assurance methodologies, Reliability Analyses like Derating analysis, Failure mode effects and criticality analysis (FMECA), Fault tree analysis (FTA) Worst case analysis, Sneak Circuit analysis, Reliability prediction, apportionment & demonstration, Test & Evaluation, Reviews etc are covered in addition to conventional Quality Control activities like Parts/Materials/ Process control and non-conformance control. The experiences gained at ISRO Satellite Centre, in realization of its highly successful Spacecraft Systems will be shared during the presentation. Various experiences / anecdotes of the presenter are embedded into the talk with a humorous touch to make the topic interesting and absorbing. Emerging concepts like First time right, Zero Defect Programme, Total Quality management (TQM) and Company wide quality control (CWQC) are also addressed. Though delivered with the background of space systems, the concepts are applicable to all fields of engineering, and gives the audience a bird’s eye view of activities to be accomplished for achieving customer satisfaction.</p>
        </div>
        <div class="bio">
            <p><strong>Bio:</strong> Mr Prakasha Rao PJVKS is currently engaged as Residential Expert – Skill Development & Satellite Systems with IN-SPACe, an autonomous body under Department of Space, formed by Government of India to promote, enable, authorize and supervise Non-Government Entities (NGEs) and academia to undertake space activities. Prior to this, he was employed in Indian Space Research Organization for over 37 years and retired as Outstanding Scientist from ISRO, and served as Director of Space Infrastructure Programme Office & Directorate of Safety, Reliability and Quality at ISRO Head Quarters. He has served in several senior positions at ISRO Satellite Centre (now URSC) like Programme Director - LEO & Planetary Platforms, Deputy Director - Spacecraft Reliability and Quality Area, Project Director GSAT 6, GSAT-9, GSAT 17 and GSAT 18 communications satellites, Associate Project Director of meteorological satellite INSAT 3D and Group Director - Systems Reliability Group. He was on deputation for about two years to Boeing Satellite Systems – USA, as consultant to Binariang Satellite Systems, Malaysia in realization of MEASAT-3 spacecraft. As Product Assurance Manager of W2M spacecraft – ISRO’s first commercial spacecraft supplied to Europe, he handled the customer interface on Product Assurance aspects. Mr. Prakasha Rao was Chairman of Project Monitoring Committee for 75 students satellite mission. He has conducted Product Assurance workshop at RCI-DRDO and was a member of faculty at several ISRO’s training programmes, ECIL, course on Satellite Engineering at IISc. He served as the Chairman of organizing committee for “UNNATI” programme a commitment given by India to United Nations for training of representatives from 30 countries on Nano Satellites and Chairman of ‘Committee to assess Security of Indian Space assets. He served as ‘Member - Committee for Promotion of Space activities’ an interim arrangement, before formal INSPACE mechanism was put in place. Mr Rao was awarded ISRO merit award in recognition of ‘meritorious contribution’ to Indian Space Programme. Considering his significant contributions to the development of Astronautics in the country, he is admitted as fellow of “Astronautical Society of India” and is member of ‘National Governing Council’ of Indian Society for Systems Engineering (ISSE).</p>
        </div>
    </div>
    <div class="workshop">
        <h2 class="title">Embedded Software Test and Integration</h2>
        <p class="speakers">Speakers: Mr. Karthik SP, Mr. Sharath Halneer, Mr. Tarun Gupta, Ms. Sintu Punnoose</p>
        <p class="time">Time: 11.00-13.00 Hrs</p>
        <p class="venue">Venue: Poinsettia 1 & 2</p>
        <div class="abstract">
            <p><strong>Abstract:</strong> The workshop titled “Embedded Software Test and Integration Test Techniques” covers essential topics related to ensuring mission-readiness in the aerospace and defence industry. Here’s a brief overview of the key areas:
                <ol>
                    <li>A Platform-Based Approach to Satellite Integration Test for Space Applications: This topic explores how to validate embedded software in satellite systems using a platform-based approach. It will cover techniques for testing various subsystems within satellites, such as integrated avionics unit, communication payload, electronics power systems, TT&C, RF Component, channel emulation etc.</li>
                    <li>Modernize Your Approach to LRU Hardware-in-the-Loop Testing: This segment focuses on improving hardware-in-the-loop (HIL) testing for LRUs. HIL testing involves integrating real hardware components with simulated environments to verify system behaviour. The session will discuss advancements in standardising the LRU testing methodologies with NI HIL tools. Learn how to integrate various simulation models into Linux Real Time targets and perform Model-in-Loop & Hardware-in-Loop testing.</li>
                    <li>Gen3 VST for Radar and Satellite System Validation: Gen3 Vector Signal Transceiver (VST) systems provide advanced capabilities for testing and validating embedded software. This will include solutions on Datalink Validation, radar Target generation, Satellite Link Emulation, Digital Tx/Rx Module Characterization (DTRM), Electronically Scanned Array Characterization (ESAC) etc.</li>
                    <li>Building Scalable Systems Integration Automated Test Equipment (ATE): Scalable ATE solutions are crucial for efficiently testing complex systems. This topic covers strategies for designing and implementing automated test equipment that can handle large-scale integration testing across LRUs.</li>
                </ol>
            </p>
        </div>
        <div class="bio">
            <p><strong>Bio:</strong> 
                <ul>
                    <li>Karthik SP: Karthik SP is a seasoned professional with over 18 years of expertise in the Aerospace & Defense industry. Holding the position of Senior Group Manager at NI (National Instruments), he leads technical teams in the JKAI region, providing innovative solutions and support to customers.</li>
                    <li>Sharath Halneer: Sharath Halneer has a robust background in test and measurement within the aerospace and defense industry. His work involves ensuring the reliability, accuracy, and performance of critical systems.</li>
                    <li>Tarun Gupta: Tarun Gupta serves as Chief Business Manager at Aerospace and Defense business unit of NI (https://www.ni.com). He has honed his skills with more than 23 years’ experience of working on System Level Design and Validation of Airborne sensors and platforms.</li>
                    <li>Sintu Punnoose: Sintu is currently working as a Senior Field Marketing Manager for Aerospace, Defence & Govt Business Unit at NI. Her current role requires her to drive collaboration with Sales & Technical team to drive the marketing strategies for the Aerospace & Defence industry for India.</li>
                </ul>
            </p>
